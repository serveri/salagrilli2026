// Precomputes the client-side flag/reward data for every challenge.
//
// For each task we take a secret `flag` and a `reward` (the value the solver
// should see after entering the right flag) and AES-256-GCM encrypt the reward
// using a key derived from the flag (PBKDF2-SHA256). Only the ciphertext, salt
// and iv are committed to the repo — they are useless without the flag, so we
// never ship the flag or the plaintext reward.
//
// GCM's auth tag doubles as the correctness check on the client: a wrong flag
// derives a wrong key and decryption throws. No server round-trip needed.
//
// Usage:
//   npm run gen:challenges
//
// Outputs:
//   src/data/challenges.js       committed  — { id, salt, iv, ct } per task
//   challenges.answers.json      git-ignored — the answer key (flags + rewards)
//
// Re-running with the same CONFIG rewrites the ciphertext with fresh salt/iv but
// KEEPS existing flags/rewards from challenges.answers.json so links you already
// shared keep working. Delete that file (or set a flag to null below) to mint a
// new random flag for a task.

import { webcrypto as crypto } from 'node:crypto'
import { readFileSync, writeFileSync, existsSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..')
const ANSWERS_PATH = join(ROOT, 'challenges.answers.json')
const OUTPUT_PATH = join(ROOT, 'src', 'data', 'challenges.js')

const PBKDF2_ITERATIONS = 150000

// ---------------------------------------------------------------------------
// Edit this. `reward` is what the solver sees after entering the correct flag —
// replace the placeholders with the real hint / piece of the final answer.
// Leave `flag` out to auto-generate a random one (recommended).
// ---------------------------------------------------------------------------
const CONFIG = [
  { id: 1, reward: 'REWARD 1 — replace with the value this task reveals' },
  { id: 2, reward: 'REWARD 2 — replace with the value this task reveals' },
  { id: 3, reward: 'REWARD 3 — replace with the value this task reveals' },
  { id: 4, reward: 'REWARD 4 — replace with the value this task reveals' },
  { id: 5, reward: 'REWARD 5 — replace with the value this task reveals' },
  { id: 6, reward: 'REWARD 6 — replace with the value this task reveals' },
  { id: 7, reward: 'REWARD 7 — replace with the value this task reveals' },
  { id: 8, reward: 'REWARD 8 — replace with the value this task reveals' },
]

const enc = new TextEncoder()
const toB64 = (bytes) => Buffer.from(bytes).toString('base64')

// Flags are compared case-insensitively and whitespace-trimmed on the client,
// so we normalise identically here before deriving the key.
const normalizeFlag = (flag) => flag.trim().toLowerCase()

function randomFlag() {
  const bytes = crypto.getRandomValues(new Uint8Array(5))
  const hex = [...bytes].map((b) => b.toString(16).padStart(2, '0')).join('')
  return `SALA{${hex}}`
}

async function deriveKey(flag, salt) {
  const baseKey = await crypto.subtle.importKey('raw', enc.encode(normalizeFlag(flag)), 'PBKDF2', false, ['deriveKey'])
  return crypto.subtle.deriveKey(
    { name: 'PBKDF2', salt, iterations: PBKDF2_ITERATIONS, hash: 'SHA-256' },
    baseKey,
    { name: 'AES-GCM', length: 256 },
    false,
    ['encrypt'],
  )
}

async function encryptReward(flag, reward) {
  const salt = crypto.getRandomValues(new Uint8Array(16))
  const iv = crypto.getRandomValues(new Uint8Array(12))
  const key = await deriveKey(flag, salt)
  const ct = await crypto.subtle.encrypt({ name: 'AES-GCM', iv }, key, enc.encode(reward))
  return { salt: toB64(salt), iv: toB64(iv), ct: toB64(new Uint8Array(ct)) }
}

async function main() {
  const previous = existsSync(ANSWERS_PATH) ? JSON.parse(readFileSync(ANSWERS_PATH, 'utf8')) : {}

  const answers = {}
  const challenges = []

  for (const entry of CONFIG) {
    const id = String(entry.id)
    // Keep a previously generated flag so shared links stay valid; only mint a
    // new one when there isn't one yet (or CONFIG pins an explicit flag).
    const flag = entry.flag ?? previous[id]?.flag ?? randomFlag()
    const reward = entry.reward

    const { salt, iv, ct } = await encryptReward(flag, reward)
    challenges.push({ id: entry.id, salt, iv, ct })
    answers[id] = { flag, reward }
  }

  const banner = '// AUTO-GENERATED by scripts/generate-challenges.mjs — do not edit by hand.\n' +
    '// Ciphertext is safe to commit; the flags live in challenges.answers.json (git-ignored).\n'
  const body = `export const challenges = ${JSON.stringify(challenges, null, 2)}\n`
  writeFileSync(OUTPUT_PATH, banner + body)
  writeFileSync(ANSWERS_PATH, JSON.stringify(answers, null, 2) + '\n')

  console.log(`\nWrote ${challenges.length} challenges → src/data/challenges.js`)
  console.log('Answer key → challenges.answers.json (git-ignored)\n')
  console.log('  task  flag                    reward')
  console.log('  ----  ----------------------  ------')
  for (const { id } of CONFIG) {
    const { flag, reward } = answers[String(id)]
    console.log(`  ${String(id).padEnd(4)}  ${flag.padEnd(22)}  ${reward}`)
  }
  console.log('')
}

main().catch((err) => {
  console.error(err)
  process.exit(1)
})
